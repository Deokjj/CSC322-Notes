#include <stdio.h>
/*---------------------------------------------------------------------------*/
int main(void) {

    int c1;

    while ((c1 = getchar()) != EOF)
        if (c1 == '\t')
                printf(" \\t ");
        else if (c1 == '\n')
                printf("\\n\n");
            else if (c1 == '\\')
                    printf("\\\\");
            else putchar(c1);

    return(0);
}
