//----Count lines in input
//-----------------------------------------------------------------------------
#include <stdio.h>
#include <stdlib.h>
//-----------------------------------------------------------------------------
int main(void) {

    int C1,Lines;

    Lines=0;
    while ((C1 = getchar()) != EOF) {
        if (C1 == '\n') {
            ++Lines;
        }
    }
    printf("\n%d\n",Lines);

    return(EXIT_SUCCESS);
}
